mv apache-tomcat-9.0.65 tomcat
cd tomcat/
cd bin
./startup.sh
cd ../..
cd tomcat/
find / -name context.xml
unalias cp
cp /dev/null /opt/tomcat/webapps/host-manager/META-INF/context.xml
